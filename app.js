function renderThemeButtons() {
  const switchWrap = document.getElementById("themeSwitch");
  switchWrap.innerHTML = "";

  Object.entries(themeData).forEach(([key, val]) => {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.className = `theme-btn${activeTheme === key ? " active" : ""}`;
    btn.textContent = val.title;
    btn.addEventListener("click", () => {
      activeTheme = key;
      renderThemeButtons();
      renderThemePanel();
    });
    switchWrap.appendChild(btn);
  });
}

function renderThemePanel() {
  const theme = themeData[activeTheme];
  const panel = document.getElementById("themePanel");

  panel.innerHTML = `
    <h3>${theme.title}</h3>
    <p>${theme.intro}</p>
    <div class="topic-tags">${theme.tags.map((tag) => `<span class="tag">${tag}</span>`).join("")}</div>
    <div class="two-col">
      <div class="mini-card">
        <h4>Faktenblatt</h4>
        <ul>${theme.facts.map((item) => `<li>${item}</li>`).join("")}</ul>
      </div>
      <div class="mini-card">
        <h4>Kernquellen</h4>
        <ul>${theme.sources.map((item) => `<li>${item}</li>`).join("")}</ul>
      </div>
      <div class="mini-card">
        <h4>Arbeitsauftrag</h4>
        <ul>${theme.tasks.map((item) => `<li>${item}</li>`).join("")}</ul>
      </div>
      <div class="mini-card">
        <h4>Mini-Quiz</h4>
        <form class="quiz-form" id="quizForm">
          ${theme.quiz
            .map(
              (item, i) => `
              <fieldset class="quiz-item">
                <legend>${i + 1}. ${item.q}</legend>
                <div class="quiz-options">
                  ${item.options
                    .map(
                      (opt, oi) => `
                    <label>
                      <input type="radio" name="q${i}" value="${oi}" /> ${opt}
                    </label>
                  `
                    )
                    .join("")}
                </div>
              </fieldset>
            `
            )
            .join("")}
          <button class="btn small" type="submit">Quiz auswerten</button>
        </form>
        <p class="quiz-result" id="quizResult"></p>
      </div>
    </div>
  `;

  const quizForm = document.getElementById("quizForm");
  quizForm.addEventListener("submit", (event) => {
    event.preventDefault();
    let correct = 0;
    const feedback = [];

    theme.quiz.forEach((q, i) => {
      const checked = panel.querySelector(`input[name=\"q${i}\"]:checked`);
      if (checked && Number(checked.value) === q.answer) {
        correct += 1;
      }
      feedback.push(`${i + 1}) ${q.explain}`);
    });

    const result = document.getElementById("quizResult");
    result.textContent = `${correct}/${theme.quiz.length} korrekt. Reflexion: ${feedback.join(" | ")}`;
  });
}

function shuffle(array) {
  const copy = [...array];
  for (let i = copy.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}

function assignRoles() {
  const rawSize = Number(document.getElementById("groupSize").value);
  const size = Number.isFinite(rawSize) ? rawSize : 5;
  const list = document.getElementById("roleList");
  list.innerHTML = "";

  const picks = shuffle(roles).slice(0, Math.max(3, Math.min(size, roles.length)));
  picks.forEach((role, i) => {
    const li = document.createElement("li");
    li.textContent = `Rolle ${i + 1}: ${role}`;
    list.appendChild(li);
  });
}

function updateTimerDisplay() {
  const minutes = String(Math.floor(timerSeconds / 60)).padStart(2, "0");
  const seconds = String(timerSeconds % 60).padStart(2, "0");
  document.getElementById("timerDisplay").textContent = `${minutes}:${seconds}`;
}

function startTimer() {
  if (timerInterval) return;
  timerInterval = setInterval(() => {
    timerSeconds -= 1;
    if (timerSeconds <= 0) {
      timerSeconds = 0;
      clearInterval(timerInterval);
      timerInterval = null;
    }
    updateTimerDisplay();
  }, 1000);
}

function pauseTimer() {
  if (!timerInterval) return;
  clearInterval(timerInterval);
  timerInterval = null;
}

function resetTimer() {
  pauseTimer();
  const rawMinutes = Number(document.getElementById("timerMinutes").value);
  const inputMinutes = Number.isFinite(rawMinutes) ? rawMinutes : 8;
  timerSeconds = Math.max(1, inputMinutes) * 60;
  updateTimerDisplay();
}

function loadChecklist() {
  const saved = localStorage.getItem(checklistKey);
  const state = saved ? JSON.parse(saved) : {};
  const checks = document.querySelectorAll("#productionChecklist input[type='checkbox']");

  checks.forEach((box) => {
    box.checked = Boolean(state[box.dataset.checkId]);
    box.addEventListener("change", () => {
      state[box.dataset.checkId] = box.checked;
      localStorage.setItem(checklistKey, JSON.stringify(state));
      updateChecklistProgress();
    });
  });

  updateChecklistProgress();
}

function updateChecklistProgress() {
  const checks = [...document.querySelectorAll("#productionChecklist input[type='checkbox']")];
  const done = checks.filter((box) => box.checked).length;
  const percent = (done / checks.length) * 100;
  document.getElementById("checklistProgress").style.width = `${percent}%`;
}

function renderScenes() {
  const body = document.getElementById("sceneTableBody");
  body.innerHTML = "";
  scenes.forEach((scene) => {
    const tr = document.createElement("tr");
    tr.innerHTML = `
      <td>${scene.title}</td>
      <td>${scene.goal}</td>
      <td>${scene.quote}</td>
      <td>${scene.camera}</td>
    `;
    body.appendChild(tr);
  });
}

function downloadFile(filename, content, type = "text/plain") {
  const blob = new Blob([content], { type });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

function slugify(input) {
  return input
    .toLowerCase()
    .trim()
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "");
}

function setupSceneForm() {
  const form = document.getElementById("sceneForm");
  form.addEventListener("submit", (event) => {
    event.preventDefault();
    scenes.push({
      title: document.getElementById("sceneTitle").value.trim(),
      goal: document.getElementById("sceneGoal").value.trim(),
      quote: document.getElementById("sceneQuote").value.trim(),
      camera: document.getElementById("sceneCamera").value.trim()
    });
    form.reset();
    renderScenes();
  });

  document.getElementById("exportStoryboardBtn").addEventListener("click", () => {
    if (scenes.length === 0) {
      return;
    }

    const lines = ["# Storyboard Salonfilm", ""];
    scenes.forEach((scene, i) => {
      lines.push(`## Szene ${i + 1}: ${scene.title}`);
      lines.push(`- Ziel: ${scene.goal}`);
      lines.push(`- Schlüsselzitat: ${scene.quote}`);
      lines.push(`- Kamera: ${scene.camera}`);
      lines.push("");
    });
    downloadFile("storyboard.md", lines.join("\n"), "text/markdown");
  });
}

function setupUploadForm() {
  const form = document.getElementById("uploadForm");
  const preview = document.getElementById("uploadPreview");

  form.addEventListener("submit", (event) => {
    event.preventDefault();

    const className = document.getElementById("className").value.trim();
    const groupName = document.getElementById("groupName").value.trim();
    const topicName = document.getElementById("topicName").value.trim();
    const videoFile = document.getElementById("videoFile").value.trim();
    const members = document.getElementById("members").value
      .split(",")
      .map((item) => item.trim())
      .filter(Boolean);
    const duration = document.getElementById("duration").value.trim();

    const isoDate = new Date().toISOString().slice(0, 10);
    const folderName = `${isoDate}_${slugify(groupName)}`;

    const manifest = {
      class: className,
      group: groupName,
      topic: topicName,
      duration,
      members,
      video_file: videoFile,
      source_focus: activeTheme,
      generated_at: new Date().toISOString(),
      target_path: `submissions/${folderName}/`
    };

    const prText = `## Salonfilm-Upload\n\n- Klasse: ${className}\n- Gruppe: ${groupName}\n- Fokus: ${topicName}\n- Dauer: ${duration}\n- Mitglieder: ${members.join(", ")}\n- Datei: ${videoFile}\n- Zielordner: submissions/${folderName}/\n\n### Quellenbasis\n- Jenny-Papers (Religion/Philosophie/Politik)\n- Transkript Knoblauchhaus\n- Transkript Biedermeier/1848\n`;

    preview.textContent = `Zielordner: submissions/${folderName}/\n\nmanifest.json und PR-Text wurden heruntergeladen.`;

    downloadFile("manifest.json", JSON.stringify(manifest, null, 2), "application/json");
    downloadFile("pull_request_text.md", prText, "text/markdown");
  });
}

